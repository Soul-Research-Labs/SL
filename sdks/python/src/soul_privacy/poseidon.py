def _sbox(x: int) -> int:
    """S-box: x^5 mod p."""
    x2 = (x * x) % FIELD_MODULUS
    x4 = (x2 * x2) % FIELD_MODULUS
    return (x4 * x) % FIELD_MODULUS


def _mds(state: list[int]) -> list[int]:
    """Apply canonical circomlib MDS matrix multiplication."""
    result = [0, 0, 0]
    for i in range(3):
        acc = 0
        for j in range(3):
            acc = (acc + MDS[i][j] * state[j]) % FIELD_MODULUS
        result[i] = acc
    return result


def _permute(state: list[int]) -> int:
    """Apply the full HADES permutation and squeeze state[0]."""
    p = FIELD_MODULUS

    # First 4 full rounds
    for rc in FULL_RC_FIRST:
        for i in range(3):
            state[i] = (state[i] + rc[i]) % p
            state[i] = _sbox(state[i])
        state = _mds(state)

    # 57 partial rounds
    for rc in PARTIAL_RC:
        state[0] = (state[0] + rc) % p
        state[0] = _sbox(state[0])
        state = _mds(state)

    # Last 4 full rounds
    for rc in FULL_RC_LAST:
        for i in range(3):
            state[i] = (state[i] + rc[i]) % p
            state[i] = _sbox(state[i])
        state = _mds(state)

    return state[0]


def poseidon_hash(left: int, right: int) -> int:
    """Hash two BN254 field elements using Poseidon T=3.

    Args:
        left: First input (will be reduced mod p).
        right: Second input (will be reduced mod p).

    Returns:
        The Poseidon hash as a field element.
    """
    state = [0, left % FIELD_MODULUS, right % FIELD_MODULUS]
    return _permute(state)


def poseidon_hash3(a: int, b: int, c: int) -> int:
    """Hash three field elements: Poseidon(Poseidon(a, b), c)."""
    return poseidon_hash(poseidon_hash(a, b), c)


def poseidon_hash_bytes(left: bytes, right: bytes) -> bytes:
    """Hash two 32-byte inputs, returning a 32-byte result."""
    l_int = int.from_bytes(left, "big") % FIELD_MODULUS
    r_int = int.from_bytes(right, "big") % FIELD_MODULUS
    result = poseidon_hash(l_int, r_int)
    return result.to_bytes(32, "big")
